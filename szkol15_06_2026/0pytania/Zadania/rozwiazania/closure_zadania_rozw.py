def konto(saldo):
    def wplac(kwota):
        nonlocal saldo
        saldo += kwota
        return saldo
    def wyplac(kwota):
        nonlocal saldo
        if kwota > saldo:
            return f"Brak srodkow (saldo: {saldo})"
        saldo -= kwota
        return saldo
    return wplac, wyplac

